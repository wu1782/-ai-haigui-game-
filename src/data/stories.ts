// 根据ID获取故事
export const getStoryById = (id: string): TStory | undefined => {
  return stories.find(story => story.id === id)
}

// 根据难度筛选
export const getStoriesByDifficulty = (difficulty: TStory['difficulty']): TStory[] => {
  return stories.filter(story => story.difficulty === difficulty)
}

// 按热度排序
export const getStoriesByHot = (): TStory[] => {
  return [...stories].sort((a, b) => b.hotScore - a.hotScore)
}

// 按最新排序
export const getStoriesByLatest = (): TStory[] => {
  return [...stories].sort((a, b) => {
    const dateA = a.createdAt ? new Date(a.createdAt).getTime() : 0
    const dateB = b.createdAt ? new Date(b.createdAt).getTime() : 0
    return dateB - dateA
  })
}

// 获取未玩过的故事
export const getUnplayedStories = (playedIds: string[]): TStory[] => {
  return stories.filter(story => !playedIds.includes(story.id))
}
